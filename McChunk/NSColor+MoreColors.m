#import "NSColor+MoreColors.h"

@implementation NSColor (NSColor_MoreColors)

+(NSColor*) goldColor {
    return [NSColor colorWithDeviceRed:1. green:0.84 blue:0. alpha:1.];
}

+(NSColor*) woodColor {
    return [NSColor colorWithDeviceRed:0.87 green:0.72 blue:0.53 alpha:1.];
}

@end
